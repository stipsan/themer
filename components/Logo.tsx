interface Props {
  spin?: number
  transition?: boolean
}
function Logo({ spin, transition }: Props) {
  const wheelRef = useRef(null)
  const logoRef = useRef(null)

  useEffect(() => {
    if (wheelRef.current && logoRef.current) {
      if (spin || transition) {
        const unit = 360 / 1.5
        const rewind = unit + Math.random() * unit
        const forward = unit + Math.random() * unit
        const animation = animate(
          wheelRef.current,
          { rotate: transition ? -rewind : forward },
          {
            // direction: transition ? 'alternate' : undefined,
            easing: spring({ stiffness: 70 }),
          }
        )
        const animation2 = animate(
          logoRef.current,
          { scale: transition ? 1.08 : 1 },
          { easing: spring({ stiffness: 70 }) }
        )
        return () => {
          if (transition) {
            animation.stop()
            animation2.reverse()
          }
        }
      }
    }
  }, [spin, transition])

  return (
    <Figure>
      <ColorWheel ref={wheelRef} />
      <StyledSanityLogo ref={logoRef} />
    </Figure>
  )
}

